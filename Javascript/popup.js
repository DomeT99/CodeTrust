function popupAlert() {
    debugger
    var b_screen = document.getElementById("black_screen");
    if (b_screen.style.display !== "block") {
        b_screen.style.display = "block";
        
    }
}
function closePopup() {
    debugger
    var b_screen = document.getElementById("black_screen");
    if (b_screen.style.display !== "none") {

        b_screen.style.display = "none";
    }
}
